                            Será entendido como assédio: comentários verbais ofensivos relacionados a gênero, idade, orientação sexual, deficiência, aparência física, tamanho, raça, religião, imagens sexuais em espaços públicos, intimidação deliberada, perseguição, assédio fotográfico, interrupção de palestra ou de alguma outra ação, contato físico inapropriado e atenção sexual indesejada.<br />

                            Os participantes que forem advertidos por qualquer comportamento de assédio devem parar imediatamente.<br />

                            Patrocinadores também estão sujeitos à política anti-assédio. Em particular, os patrocinadores não devem usar imagens, atividade ou outro material com contexto sexual. Recepcionistas de estandes (incluindo voluntários) não devem usar roupas/uniformes/trajes inadequadas (sexualmente atrativas), ou criar, de alguma forma, um ambiente sexualizado.<br />

                            Se um participante se envolve em comportamento de assédio, os organizadores da conferência poderão tomar qualquer ação que considerem adequada, inclusive advertindo o infrator ou até mesmo o expulsando da conferência, sem restituição.<br />

                            Se você estiver sendo assediado, ou perceber que alguém está sendo assediado, ou tiver alguma outra dúvida, entre em contato com um membro da equipe da conferência imediatamente. A organização do evento pode ser facilmente identificada pelas camisas com a marca do evento.<br />

                            A organização da conferência terá o prazer em ajudar os participantes a contatar a segurança do local do evento ou até mesmo oficiais da lei local (polícia), fornecer escoltas, ou ajudar aqueles que sofreram o assédio para que estes se sintam seguros durante a conferência. Nós valorizamos a sua participação.<br />

                            Esperamos que os participantes sigam estas regras na conferência, salas de workshops e outros eventos sociais relacionados à conferência.<br />

